    7)
        echo "\033[11;6H\033(0a\033(B\033(0a\033(B\033(0a\033(B\033(0a\033(B\033[12;8H\033(0a\033(B\033[13;8H\033(0a\033(B\033[14;8H\033(0a\033(B\033[15;8H\033(0a\033(B\033[16;8H\033(0a\033(B"
