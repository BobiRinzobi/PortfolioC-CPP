        ;;
    2)
        echo "\033[11;6H\033(0a\033(B\033(0a\033(B\033(0a\033(B\033[12;8H\033(0a\033(B\033[13;6H\033(0a\033(B\033(0a\033(B\033(0a\033(B\033[14;6H\033(0a\033(B\033[15;6H\033(0a\033(B\033(0a\033(B\033(0a\033(B\033[16;6H\033(0a\033(B\033(0a\033(B\033(0a\033(B"
        ;;
    3)
        echo "\033[11;6H\033(0a\033(B\033(0a\033(B\033(0a\033(B\033[12;8H\033(0a\033(B\033[13;6H\033(0a\033(B\033(0a\033(B\033(0a\033(B\033[14;8H\033(0a\033(B\033[15;8H\033(0a\033(B\033[16;6H\033(0a\033(B\033(0a\033(B\033(0a\033(B"
        ;;
    4)
        echo "\033[11;6H\033(0a\033(B   \033(0a\033(B\033[12;6H\033(0a\033(B   \033(0a\033(B\033[13;6H\033(0a\033(B\033(0a\033(B\033(0a\033(B\033(0a\033(B\033[14;8H\033(0a\033(B\033[15;8H\033(0a\033(B\033[16;8H\033(0a\033(B"
        ;;
    5)
        echo "\033[11;6H\033(0a\033(B\033(0a\033(B\033(0a\033(B\033(0a\033(B\033[12;6H\033(0a\033(B\033[13;6H\033(0a\033(B\033(0a\033(B\033(0a\033(B\033[14;8H\033(0a\033(B\033[15;8H\033(0a\033(B\033[16;6H\033(0a\033(B\033(0a\033(B\033(0a\033(B"
        ;;
    6)
        echo "\033[11;6H\033(0a\033(B\033(0a\033(B\033(0a\033(B\033(0a\033(B\033[12;6H\033(0a\033(B\033[13;6H\033(0a\033(B\033(0a\033(B\033(0a\033(B\033[14;6H\033(0a\033(B   \033(0a\033(B\033[15;6H\033(0a\033(B   \033(0a\033(B\033[16;6H\033(0a\033(B\033(0a\033(B\033(0a\033(B"
